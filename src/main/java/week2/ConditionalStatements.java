package week2;

public class ConditionalStatements {

	public static void main(String[] args) {
		
		
		int i = 10;
		
		if(i>20) {
			
			System.out.println("The value of i is greater than 20");
			
		}
		
		if(i<20) {
			
			System.out.println("The value of i is lesser than 20");
			
		}
		
		int a = 100;
		
		int b = 25;
		
		boolean bval = a>b;
		
		if(bval) {
			
			System.out.println("a is greater than b");
			
		}

	}

}
